public class Pertemuan2 {
    public static void main(String[] args) {
        // Deklarasi variabel x dengan tipe data integer
        int x;

        // Memberi nilai awal (inisialisasi) ke variabel x
        x = 34;
        // Menampilkan nilai awal dari x
        System.out.println("Nilai x semula: " + x);

        // Memberi nilai baru ke variabel x
        x = 45;
        // Menampilkan nilai x setelah diubah
        System.out.println("Nilai x baru: " + x);
    }
}